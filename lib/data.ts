export const getWilayaDisplayName = (title: string, language: "ar" | "en" | "fr" = "ar") => {
  const baseWilaya = title.split("(")[0].trim().replace(/\d+$/, "").trim()
  const wilayaInfo = wilayaNames[baseWilaya]

  if (!wilayaInfo) return title

  if (language === "ar") {
    return `${wilayaInfo.arabic} - ${wilayaInfo.latin}`
  }
  return `${wilayaInfo.latin} - ${wilayaInfo.arabic}`
}
